#include <stdio.h>

int main()
{
    int a;
    int b;
    scanf("%d", &a);
    scanf("%d", &b);
    if (a > 0)
    {
        printf("A is positive number\n");
    }
    else
    {
        printf("A is negative number\n");
    }
    if (b > 0)
    {
        printf("B is positive number\n");
    }
    else
    {
        printf("B is negative number\n");
    }
}